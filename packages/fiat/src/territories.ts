export const currencyMatchesTerritory = (
    currencyCode: CurrencyCode,
    searchTerm: string,
): boolean => {
    const term = searchTerm.toLowerCase();

    return (CURRENCY_TERRITORIES[currencyCode] ?? []).some(t =>
        t.name.toLowerCase().includes(term),
    );
};
